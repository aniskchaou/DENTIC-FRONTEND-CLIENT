const ServicePage = require("../models/service.page.models");
const HomePage = require("../models/home.page.models");
const BlogPage = require("../models/blog.page.models");
const OpeningPage = require("../models/openinghours.page.models");

exports.getHomePage = (res) => {
    HomePage.findOne()
        .then(data => {
            res.send(data);
        })
        .catch(err => {
            res.status(500).send({
                message:
                    err.message || "Some error occurred while retrieving users."
            });
        });
}

exports.updateHomePage = (id, req, res) => {
    HomePage.update(req.body, {
        where: { id: id }
    })
        .then(num => {
            if (num == 1) {
                res.send({
                    message: "User was updated successfully."
                });
            } else {
                res.send({
                    message: `Cannot update User with id=${id}. Maybe User was not found or req.body is empty!`
                });
            }
        })
        .catch(err => {
            res.status(500).send({
                message: "Error updating User with id=" + id
            });
        });
}

